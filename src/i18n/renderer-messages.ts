export type RendererMessageKey = keyof typeof EN_MESSAGES;
export type RendererTranslator = (
  key: RendererMessageKey,
  params?: Record<string, string | number>
) => string;

const MESSAGE_BY_LOCALE: Record<SupportedLocale, Record<RendererMessageKey, string>> = {
  en: EN_MESSAGES,
  ko: KO_MESSAGES,
};

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_full, key) => {
    const value = params[key];
    return value === undefined ? '' : String(value);
  });
}

export function createRendererTranslator(locale: SupportedLocale): RendererTranslator {
  return (key, params) => {
    const template = MESSAGE_BY_LOCALE[locale][key] ?? MESSAGE_BY_LOCALE.en[key];
    return interpolate(template, params);
  };
}
